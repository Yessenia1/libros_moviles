class Product {
  final String id;
  final String title;
  final String description;
  final String genre;
  final String synopsis;
  final double price;
  final String imageUrl;

  Product({
    required this.id,
    required this.title,
    required this.description,
    required this.genre,
    required this.synopsis,
    required this.price,
    required this.imageUrl,
  });
}
